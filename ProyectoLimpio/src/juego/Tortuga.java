package juego;

public class Tortuga {
	private int x;
	private int y;
	private int ancho;
	private int alto;
	private int velocidad
	;
	
	
	public Tortuga(int x, int y, int ancho, int alto, int velocidad) {
		super();
		this.x = x;
		this.y = y;
		this.ancho = ancho;
		this.alto = alto;
		this.velocidad = velocidad;
	}
	
	
}
